#ifndef __EQUALS_H__
#define __EQUALS_H__

/*
Determine if the 2 values are exactly the same.
[params]
- a: first value
- b: second value
[return]
Return true if `a` == `b` or both `a` and `b` are nan, false other else.
*/
bool Equals(double a, double b);

#endif // __EQUALS_H__